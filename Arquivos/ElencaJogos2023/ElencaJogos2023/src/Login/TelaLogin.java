    private void botaoAdmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAdmActionPerformed
        JTextField campoEmail = new JTextField();
        JPasswordField campoSenha = new JPasswordField();

        Object[] message = {
            "Email:", campoEmail,
            "Senha:", campoSenha
        };

        int option = JOptionPane.showConfirmDialog(null, 
                message, "Login", JOptionPane.OK_CANCEL_OPTION);
        if (option == JOptionPane.OK_OPTION) {
            String email = campoEmail.getText();
            String senha = new String(campoSenha.getPassword());

            if (email.equals("brunopz@gmail.com") && senha.equals("963")) {
                AdmTelaInterna admTela = new AdmTelaInterna();
                jDesktopPane1.add(admTela);
                admTela.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, 
                        "Você não é o Administrador do Sistema. Por favor, não use este botão.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Campo "
                    + "exclusivo para o Administrador do Sistema");
        }

    }//GEN-LAST:event_botaoAdmActionPerformed

