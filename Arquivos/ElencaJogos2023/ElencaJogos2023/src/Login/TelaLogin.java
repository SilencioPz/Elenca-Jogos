    private void botaoLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoLoginActionPerformed
        try {
            UsuarioDAO dao = new UsuarioDAO();

            String email = campoEmail.getText();
            String senha = campoSenha.getText();

            if (email == null || !email.endsWith("@gmail.com")) {
                throw new Exception("E-mail deve terminar com '@gmail.com'!");
            }

            if (senha == null || senha.isEmpty()) {
                throw new Exception("Senha não pode ser nula!");
            }

            if (dao.verLogin(email, senha)) {
                PrincipalTelaInterna telaPrincipal = new PrincipalTelaInterna();
                jDesktopPane1.add(telaPrincipal);
                telaPrincipal.setVisible(true);
            } else {
                throw new Exception("Informação incorreta. Por favor, insira um e-mail e senha cadastrados.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_botaoLoginActionPerformed
    
